package day02;

public class c4_arithmeticOperators {
    public static void main(String [] args){
        int i1=1;
        int i2=2;

        double d1=1.5;
        double d2=2.5;
        // sum of two numbers
        System.out.println(i1+i2);
        System.out.println(d1+d2);
        System.out.println(i1+d2);
        System.out.println(i2+d2);

        //minus operator
        System.out.println(i1-i2);
        System.out.println(d1-d2);
        System.out.println(i1-d2);
        System.out.println(i2-d2);

        //multiple numbers
        System.out.println(i1*i2);
        System.out.println(d1*d2);
        System.out.println(i1*d2);
        System.out.println(i2*d2);

        //division
        System.out.println(i1/i2);
        System.out.println(d1/d2);
        System.out.println(20.0/10);// result = 2.0
        System.out.println(20/10);// result = 2

        System.out.println(10/4); //result = 2 because of integer
        System.out.println(11.0/4); // result =2,75
        System.out.println(11/4.0);// result 2,75

        //reminder
        System.out.println(10%2);
        System.out.println(7%5);
        System.out.println(10%3);







    }
}
