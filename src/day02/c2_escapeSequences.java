package day02;

public class c2_escapeSequences {

    public static void main(String[] args){
        System.out.println("RustemErdem");
        // " \t " this will give an extra space in your code
        System.out.println("\tRustemErdem");
        System.out.println("\t\t\tRustemErdem");
        // " \n " means new line text will be displayed on new line
        System.out.println("RustemErdem");
        System.out.println("Rustem\nErdem");
        System.out.println("Rustem\n\tErdem");
        // " \\ " this will print single slash on the console
        System.out.println("\\");
        System.out.println("/");
        // you have text inside text you want to use
        System.out.println("today is \"saturday\"");
        //to have single quote
        System.out.println("today is \'saturday\'");






    }
}
