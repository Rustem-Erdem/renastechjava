package day03;

public class c1_rationalOperators {
    public static void main(String [] args){
        //create variables
        int a=10;
        int b=10;

        System.out.println(a==b); //equal operator
        System.out.println(a!=b);// not equal
        System.out.println(a>b);// bigger
        System.out.println(a<b);// smaller
        System.out.println(a>=b);
        System.out.println(a<=b);

    }
}
